export type TranslationKey = keyof typeof FALLBACK_TRANSLATIONS.en;

export type TranslationMap = Record<TranslationKey, string>;

export type TranslationBundle = Record<Language, TranslationMap>;

export type TranslationParams = Record<string, string | number>;

export function isLanguage(value: string | null | undefined): value is Language {
  return value === "en" || value === "ms";
}

export function mergeTranslations(
  remote?: Partial<Record<Language, Partial<Record<string, string>>>> | null,
): TranslationBundle {
  return {
    en: { ...FALLBACK_TRANSLATIONS.en, ...(remote?.en ?? {}) },
    ms: { ...FALLBACK_TRANSLATIONS.ms, ...(remote?.ms ?? {}) },
  } as TranslationBundle;
}

export function translate(
  translations: TranslationBundle,
  language: Language,
  key: TranslationKey,
  params: TranslationParams = {},
): string {
  const template = translations[language]?.[key] ?? FALLBACK_TRANSLATIONS.en[key] ?? key;

  return template.replace(/\{(\w+)\}/g, (_, param: string) =>
    params[param] === undefined ? `{${param}}` : String(params[param]),
  );
}
